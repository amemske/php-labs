 *
 *  UTF-8 and UTF-16 are variable-length encodings that represent Unicode characters using different byte sequences.
 *  UTF-8 is widely used on the internet due to its compatibility with ASCII,
 *  while UTF-16 is commonly used in systems with native 16-bit representations such as Windows.
 *  e.g. UTF-8 representation of "A" is the same as its ASCII representation, which is 0x41.
 *  e.g. The code point U+0041 which is "A" directly corresponds to the UTF-16 representation 0x0041.
